       <h1 class="text-center chewy">Cuento Venezolano Tio Tigre y Tio Conejo</h1>
       <br>
        <div class="d-inline-flex">
        <img class="col-4" src="../Imagenes/1.jpg" alt="">
         <p class="col-7 text-justify">
         El pobre tío Tigre estaba echado debajo de una mata de mangos relamiéndose sus pobres patas ya que un Puerco Espín le había lanzado todas sus espinas por todo su pobre cuerpo. Ese Tío Conejo si es mañoso y astuto. Se dio cuenta a tiempo de que lo había engañado ofreciéndole una mano de cambur para comérmelo, y ni corto ni perezoso llamo al tío cachicamo que a su vez era muy amigo del Puerco Espin, y no me dio tiempo ni siquiera de ponerle una garra encima gimiendo todavía por el dolor que le habían producido las heridas de las espinas.</p>
          </div>
        
         
          <p>Sin embargo, en ese momento paso un gran zamuro cerca de Tío tigre diciéndole: - Quieres comerte a Tío Conejo, yo te puedo ayudar, pero eso sí, me tienes que dar una parte de los animales que caces para tus comidas. Tío Tigre no muy convencido lo observo, pero al final acepto:- Pero, ¿cómo? ese conejo es muy mañoso. El Zamuro le indico: - Detrás de esa gran sembradía de cambures, y al final de una gran hilera de matas de mangos y lechosas corre un riachuelo, allí se encuentra escondido en el fondo del rió una gran olla, dicen que es la olla de los deseos, y cualquier deseo que pidas se te puede cumplir, incluso atrapar al mañoso de Tío Conejo. Tío Tigre muy entusiasmado se afilo sus grandes garras imaginándose que todo seria muy fácil, y ya se estaría comiendo a tío conejo.</p>

          <p>Sin embargo, una iguana lo había escuchado todo, y se fue a donde vivía Tío Conejo. Mira Tío Conejo, Tío Tigre te tiene una trampa, te va comer pero eso si consigue la olla de los deseos. La olla se encuentra en el río que se encuentra cerca de la hilera de matas de mangos y lechosas. – Pues, yo voy a descubrir primero esa olla. A mi no me comen tan fácilmente- Dijo Tío Conejo.</p>

           <p>A pesar de que Tío Tigre era muy veloz, Tío Conejo también muy ingenioso, así que le dijo su amigo canela, el caballo que habitaba cerca de su madriguera que lo llevase al río porque ya Tío Tigre llevaba la delantera, explicándole en el camino todo lo referente a la olla. Cuando Tío Conejo ya había llegado al río, Tío Tigre estaba a poco metros de distancia, y este ya había divisado donde estaba la olla.</p>

            <p>Sin embargo, debajo de los matorrales se encontraba una mofeta. Tío Conejo la llamo:- Mira, allí esta Tío Tigre, y te voy a decir un secreto, el tiene planeado mudarse para acá para comerse todos los animales que viven por este sector. Tú eres el único que los puedes salvar. Acércate sigilosamente, y lanza tu perfume, y así saldrá corriendo, y no se le ocurrirá venir mas por estas regiones encantadoras- La mofeta, que de solo la idea de pensar que iba ser el bocadillo de ese felino o alguno de sus amigos no pierdo tiempo, y se le acerco a Tío Tigre. _ Hola, Tío Tigre. ¿Qué haces por aquí? -Lanzándole en ese momento a Tío Tigre su delicioso perfume. Y el pobre Tío Tigre cubriéndose de la hediondez del perfume de la mofeta se lanzo al río. En ese momento, Tío Conejo encontró la olla y se la llevo corriendo a su guarida, y pidió a la olla mágica que le trajera siempre zanahorias, y frutas en los tiempos de lluvia, y cómo el era muy generoso lo compartió con todos sus amigos. Y colorin colorado este cuento se ha acabado.</p>
