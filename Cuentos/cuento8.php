       <h1 class="text-center chewy">La Familia De Los Matea</h1>
       <br>
        <div class="d-inline-flex">
        <img class="col-4" src="../Imagenes/13.jpg" alt="La Familia De Los Matea">
         <p class="col-7 text-justify">
         Había una vez en el campo, una familia de Mates. Eran papá Mate, mamá Mate, nena Mate, nene Mate y bebé Mate. Vivían en una casita chiquita muy colorida, llena de flores y árboles que guardaban caramelos dulces, tan dulces como el azúcar.</p>
          </div>
        
         
          <p>Y en la ciudad, vivía la otra familia de Mates: pero ellos eran solo papá Mate, mamá Mate y bebé Mate. Ellos, en cambio, vivían en un departamento grande, sin color, ni alegría, ni caramelos guardados en el pantalón de las ilusiones. Con sus vecinos los otros Mates de ciudad, que hablaban otros idiomas, se vestían diferentes, sus colores eran otros. ¡Cómo se extrañaban, los Mates del campo y la ciudad! ¡Cómo se querían! La distancia entre la casita y el departamento era muy grande, y solo existían los micros que llevaban a cada Mate del país, porque los humanos podían viajar en auto, micro, y hasta avión.</p>

          <p>Hasta que algo sucedió. Al bebé Mate del campo comenzó a dolerle su pancita de madera, porque había comido muchos caramelos de los árboles. El pobrecito lloraba, lloraba, lloraba… así que decidieron ir a la ciudad. No tenían celulares como si tienen los de la ciudad, entonces escribieron una carta con la bombilla a su familia del departamento para que los hospedaran. El invierno se aproximaba y el frio empezaba a crecer… ¡Como el dolor de panza del Mate bebé!, y esa era la fecha en la que tenía que ir al Hospital para que los médicos humanos lo observaran y le dieran una receta para que se curara.</p>

           <p>Después de unos días la carta llegó al departamento en la ciudad anunciando la próxima visita de los Materos camperos. ¡Que contentos se pusieron los Mates del Obelisco! Cuando fue el día en el que en el micro donde viajó la familia de los Mates de bombacha y alpargatas llegó a la ciudad, la familia de los Mates de Jeans y Red Social los recibieron con los brazos abiertos. ¡Al fin se reunieron! Por supuesto, tomaron mate.</p>

            <p>El Mate bebé fue al Hospital con mamá Mate y el Doctor le dio un remedio para curar su empacho. Nada grave: solo le dolía la pancita. ¡Menos mal! Por eso a la familia de los Mates de la ciudad se le ocurrió hacer una fiesta. ¡Cómo se divirtieron!, ¡Cuánto hace que la familia no estaba tan unida! A la nena Mate de la ciudad se le ocurrió que todos se sacaran una selfie, y aunque los del campo muy bien no sabían que era eso, sonrieron y le pusieron toda su alegría. Entre mate y mate, ambas familias disfrutaron el momento, salieron a pasear, tomaron helado, comieron hamburguesas y jugaron en las plazas, como lo hacen los humanos. Los Mates del campo así, descubrieron que había otras culturas, otros idiomas, otras formas de vestir. Y los Mates de la ciudad, asombrados, supieron que la humildad y la sencillez es lo mejor que podemos tener todos. </p>

            <p>Finalmente los Mates del campo volvieron a su casita chiquita y colorida, y los Mates de la ciudad se quedaron en su departamento triste y gris. Cuando la nena Mate porteña vio la foto que se habían sacado todos, la puso en un portarretratos de cañas para que siempre estuvieran presentes. El tiempo pasó, llegó fin de año y los Mates de la ciudad fueron en micro a visitar a los Mates del campo. ¡Qué lindo!, llegaron las vacaciones y por eso todos pasaron el verano en el campo, bañándose en el rio, subiéndose a los árboles y comiendo caramelos, ¡Nunca se hacían largos los días!, ¡Que divertido! Llegó Marzo y el Mate bebé cumplía su primer añito, por eso el mejor regalo fue tener a su familia de Jeans y Red Social. Esta vez, la nena Mate del campo decidió sacar la selfie y quedó para siempre la foto de una familia distinta, pero igual. La familia de los Mates, que siempre tomaba mate.</p>
