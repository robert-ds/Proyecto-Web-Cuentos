       <h1 class="text-center chewy">El Cuarto de Espejos. Cuentos de Princesa</h1>
       <br>
        <div class="d-inline-flex">
        <img class="col-4" src="../Imagenes/11.jpg" alt="">
         <p class="col-7 text-justify">
         El cuarto de espejos narra la historia de una princesa que era muy presumida y vanidosa. No le importaba más nadie que ella misma. Ni su familia, ni su servidumbre, nadie. A todos los trataba con indiferencia; no necesitaba a nadie, ni quería a nadie. Un día vino un hada madrina como invitada a su casa y la muchacha la trato mal, los padres quedaron horrorizados, no sabían que hacer con su hija. El hada madrina como castigo le dijo a la muchacha:</p>
          </div>
        
         
          <p>Si crees que no necesitas a más nadie, y sólo te quieres a ti, vivirás sola, con tu propio reflejo hasta que tu corazón cambie.<br/>
          La princesa fue encerrada en un cuarto de espejos, no podía salir, y día tras día lo único que veía era su reflejo en todos lados. Así pasaron años; se despertaba y lo único que veía era su cara reflejada en los espejos del cuarto; arriba, abajo, a los lados, en todo. Un día después de varios años la princesa empezó a sentirse sola y pensó que quizás no era mejor estar siempre sola, que quizás sí necesitaba a su familia, los extrañaba. A medida que iba progresando con estos sentimientos un espejo se tornaba invisible, y podía ver a través de el hacia afuera del cuarto. En un rincón veía al padre sentado escribiendo, en otro lado estaba su madre tejiendo. En la cocina veía su niñera que la había criado y era como una segunda madre, mas abajo en el jardín veía al hijo de la niñera, que estaba podando las rosas, él también había sido como un hermano, y recordó cuando jugaban juntos de niños. Poco a poco a medida que iba sintiendo afecto por todos ellos, los espejos iban desapareciendo y veía más y más hacia afuera, lo que se perdía, lo que extrañaba, lo que anhelaba. Hasta que llegó el día en que todo se tornó invisible y no aguantó las ganas y abrió la puerta y salió del cuarto. Al salir, toda su familia se contentó, ella lloró y los abrazó. Los extrañaba tanto…
          </p>

          <p>En esto llega el hada madrina; la princesa se asusta y le dice que no la vuelva a encerrar, no lo podría tolerar más, que ya aprendió, que no la vuelva a meter allí, ya no puede despertar día tras día viéndose solo ella misma en ese cuarto de espejos. El hada sonríe y le responde que ella nunca la encerró allí, que solo la metió, y fue su orgullo el que hizo que nunca abriera la puerta. Que ella pudo haber salido de allí en cualquier momento que hubiera querido. Pero su orgullo nunca le dejó tomar ese primer paso. Ella asumió lo peor y actuó en base a ello. Luego le dijo que el cuarto no era un cuarto de espejos, era un cuarto de cristal, y que siempre pudo haber visto a través de él, pero que a causa de sus sentimientos, lo único que veía reflejado en el cristal, era su propio reflejo. Y tenia que pasar un tiempo a solas antes de que empezara a ver a través de él, mas allá de ella misma, para poder ver las personas a su alrededor, cuánto la querían y cuánto ella los necesitaba. Que no tuviera miedo que eso ya no le volvería a pasar; ya era libre, que siempre lo fue, pero ahora lo era de corazón.</p>

           <p>A pesar de que Tío Tigre era muy veloz, Tío Conejo también muy ingenioso, así que le dijo su amigo canela, el caballo que habitaba cerca de su madriguera que lo llevase al río porque ya Tío Tigre llevaba la delantera, explicándole en el camino todo lo referente a la olla. Cuando Tío Conejo ya había llegado al río, Tío Tigre estaba a poco metros de distancia, y este ya había divisado donde estaba la olla.</p>

            <p>Sin embargo, debajo de los matorrales se encontraba una mofeta. Tío Conejo la llamo:- Mira, allí esta Tío Tigre, y te voy a decir un secreto, el tiene planeado mudarse para acá para comerse todos los animales que viven por este sector. Tú eres el único que los puedes salvar. Acércate sigilosamente, y lanza tu perfume, y así saldrá corriendo, y no se le ocurrirá venir mas por estas regiones encantadoras- La mofeta, que de solo la idea de pensar que iba ser el bocadillo de ese felino o alguno de sus amigos no pierdo tiempo, y se le acerco a Tío Tigre. _ Hola, Tío Tigre. ¿Qué haces por aquí? -Lanzándole en ese momento a Tío Tigre su delicioso perfume. Y el pobre Tío Tigre cubriéndose de la hediondez del perfume de la mofeta se lanzo al río. En ese momento, Tío Conejo encontró la olla y se la llevo corriendo a su guarida, y pidió a la olla mágica que le trajera siempre zanahorias, y frutas en los tiempos de lluvia, y cómo el era muy generoso lo compartió con todos sus amigos. Y colorin colorado este cuento se ha acabado.</p>
