       <h1 class="text-center chewy">Sacrificio de Amor</h1>
       <br>
        <div class="d-inline-flex">
        <img class="col-4" src="../Imagenes/12.jpg" alt="Sacrificio de Amor">
         <p class="col-7 text-justify">
           Ana era una mujer encantadora, sus ojos eran dos ventanales hacia el misterio de las emociones. Su cabellera era abundante, larga y negra. Su piel era un poco bronceada. Su rostro siempre estaba decorado con una sonrisa radiante, hasta el Sol sentiría envidia ante tanto esplendor.</p>
          </div>
        
         
          <p>Ana era viuda, tenía dos hermosa hijas, dignas de cualquier elogio, tan hermosas como la madre, pero muy traviesas. Tanto, que Ana se acostaba exhausta de tanto correr y estar pendiente de las traviesas niñas. Ella había cerrado toda esperanza de amor en su corazón, su vida se centraba en sus dos hijas.</p>

          <p>Pasaron dos años y ella comenzó a compartir con un nuevo amigo, siempre él tenía la misma excusa para verla. Le invitaba a tomar un café. Era educado, amable y muy dulce. Pero sobre todo se ganó el amor de sus dos pequeñas traviesas.</p>

           <p>Así pasó algún tiempo y Ana se sentía sola. El amigo, de ella se enamoró, le propuso matrimonio y tiernamente ella aceptó. Pasaron algunos meses y un tormento en ella nació, un viejo amor regresaba y toda su vida se nubló.</p>

            <p>Su vida ahora era triste, pensando en su viejo amor, igual aquél sufría viendo que ella se casó. Ella se separó de aquél hombre noble y bueno, para intentar una nueva vida con ese hombre que había regresado de muy lejos.</p>

            <p>Pero lo que Ana no sabía era que aquel hombre estaba acostumbrado a la soledad, al silencio, a la independencia, lo cual fue un terrible choque entre el comportamiento travieso de sus hijas que tanto lo irritaba.</p>

            <p>Una torre de cristal de ilusiones se derrumbaba en un solo día. Ana era una mujer un poco impetuosa y tomó una decisión arrebatadora. Volvería a su casa con sus hijas. Nada la detuvo, pues, una vez que se alejó de esa casa una frase escrita por él en las redes sociales la marcaría de por vida…”La Paz no tiene precio”</p>

            <p>Ella sintió que su mundo interior se desplomaba, que moría, que su alma se iba deshojando como los árboles en otoño… un frío indescriptible recorrió su cuerpo y peor aún… su alma. </p>

            <p>Pasaron los días y alguien esperaba en silencio, en la oscuridad. Alguien con los brazos abiertos, con el alma llena de amor para dar. Allí seguía, esperando, dispuesto a coser aquel corazón roto con hilos de esperanza.</p>

            <p>Pasó el tiempo y con gestos de afecto, sinceridad, su antiguo esposo le ofreció cobijo, formar un hogar y Ana aceptó. Comprendió que quien te ama te acepta con tus defectos y virtudes. Sus hijas pilares fundamentales en su vida, se sentían felices, estables. Ella echó a andar su vida, pues ésta no se detiene y mucho menos el tiempo, el cual es inclemente y no perdona.</p>
